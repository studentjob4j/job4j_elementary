package ru.job4j.pojo;

public class Student {

    private String family;
    private String name;
    private int numberGroop;
    private int dataPostuplenia;

    public String getFamily() {
        return family;
    }

    public String getName() {
        return name;
    }

    public int getNumberGroop() {
        return numberGroop;
    }

    public int getDataPostuplenia() {
        return dataPostuplenia;
    }

    public void setFamily(String family) {
        this.family = family;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setNumberGroop(int numberGroop) {
        this.numberGroop = numberGroop;
    }

    public void setDataPostuplenia(int dataPostuplenia) {
        this.dataPostuplenia = dataPostuplenia;
    }
}
